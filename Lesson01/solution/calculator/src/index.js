function sum(number1, number2) {
	return number1 + number2;
}


function subtract(number1, number2) {
	return number1 - number2;
}


function multiple(number1, number2) {
	return number1 * number2;
}


function divide(number1, number2) {
	return number1 / number2;
}

export const calculator = {
	sum,
	subtract,
	multiple,
	divide
}
